package com.iiit.db;

public class Attribute {

	public String name;
	public String type;
	public Integer index;
	public Attribute(String name, String type, Integer index) {
		super();
		this.name = name;
		this.type = type;
		this.index = index;
	}
	
	
	public Attribute() {
		super();
	}
	
	
}
